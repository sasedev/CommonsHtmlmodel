namespace Sase\Commons\Htmlmodel\Attribute\Option;

use Sase\Commons\Htmlmodel\AbstractAttribute;

/**
 * SelectedAttribute
 * <p>
 * The selected attribute is a boolean attribute.<br>
 * When present, it specifies that an option should be pre-selected when the page loads.<br>
 * The pre-selected option will be displayed first in the drop-down list.<br>
 * Tip: The selected attribute can also be set after the page loads, with a JavaScript.
 * </p>
 * <p>
 * Browser Support:
 * <ul>
 * <li>Chrome: Yes</li>
 * <li>Internet Explorer: Yes</li>
 * <li>Firefox: Yes</li>
 * <li>Opera: Yes</li>
 * <li>Safari: Yes</li>
 * </ul>
 * </p>
 *
 * @author <a href="mailto:seif.salah@gmail.com">sasedev</a>
 */
class SelectedAttribute extends AbstractAttribute
{

	/**
	 * Name of html attribute
	 *
	 * @var string KEY = "selected"
	 */
	protected $KEY = "selected";

	/**
	 * Contructor
	 */
	public function __construct()
	{
	}

	/**
	 * Set $value
	 *
	 * @param mixed $value
	 *
	 * @throws \Exception
	 */
	public function setValue($value)
	{
		throw new \Exception('No values are accepted for this attribute');
	}

	/**
	 * check the list of html elements that can add this Attribute
	 *
	 * @return string[]
	 */
	public function getAppliesTo()
	{
		$coveredElements = array(
			'option'
		);

		return $coveredElements;
	}

	/**
	 * Default string representation
	 */
	public function __toString()
	{
		return $this->getKey();
	}
}